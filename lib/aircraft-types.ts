export function getAircraftDisplayName(designator: string): string {
  const normalized = designator.trim().toUpperCase()
  if (!normalized || normalized === "N/A") return "N/A"

  return AIRCRAFT_TYPE_NAMES[normalized]
    ? `${AIRCRAFT_TYPE_NAMES[normalized]} (${normalized})`
    : `ICAO type ${normalized}`
}
